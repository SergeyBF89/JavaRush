package org.level_2_lecture_3_by_8.lecture_5;

public class Strings {
    public static void main(String[] args) {
        // В методе main объяви переменные word, phrase, line и text типа String.
        //Присвой им разные значения. Используй одну команду для создания и инициализации переменных.
        //Значениями могут быть любые строки.

        // Требования:
        //•	В методе main объяви четыре переменные типа String.
        //•	Назови переменные именами word, phrase, line и text.
        //•	Переменным сразу присвой значения.
        //•	У всех переменных должны быть разные значения.

        String word = "Hello word", phrase = "Прайс", line = "Линия", text = "Текст";
        System.out.println(word + ", " + phrase + ", " + line + ", " + text);
    }
}
